package src.java.basic.variable;

/**
 * @author : zgf
 * @file : JavaVariable.java
 * @brief : Java变量
 */


public class JavaVariable {
    public static void main(String[] args) {
        //定义变量
        int a = 10;

        //输出变量
        System.out.println(a);

        //修改变量
        a = 20;
        System.out.println(a);

        //定义byte类型的变量
        byte b = 10;
        System.out.println(b);

        //定义short类型的变量
        short s = 100;
        System.out.println(s);

        //定义int类型的变量
        int i = 10000;
        System.out.println(i);

        //定义double类型的变量
        double d = 13.14;
        System.out.println(d);

        //定义char类型的变量
        char c = 'a';
        System.out.println(c);

        //定义boolean类型的变量
        //boolean b = true;
        //System.out.println(b);
        boolean bb = true;
        System.out.println(bb);
        System.out.println("--------");

        //定义long类型的变量
        long l = 10000000000L;
        System.out.println(l);
        System.out.println("--------");

        //定义float类型的变量
        float f = 13.14F;
        System.out.println(f);
    }

}
